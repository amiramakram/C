#include <stdio.h>
#include <stdlib.h>

int main()
{
    //sum
    int x=3;
    int y=4;
    int sum=x+y;

    printf("value of sum= %d\n",sum);
    //subtraction
    int sub=y-x;

    printf("value of sub= %d\n",sub);
    //division

    int s=2;
    int div=y/s;
    printf("value of div= %d\n",div);
    //multiplication
    int mult=x*y;
    printf("value of mult= %d",mult);

    return 0;
}
